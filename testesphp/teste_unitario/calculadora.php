<?php

class calculadora //Cria-se uma classe
{
    public function add($a, $b) //Função pública chamado add passando dois parâmetros $a e $b
    {
        return $a + $b; //Retorna o valor de $a + $b
    }

    public function subtract($a, $b) //Função pública chamado subtract passando dois parâmetros $a e $b
    {
        return $a - $b; //Retorna o valor de $a - $b
    }
}